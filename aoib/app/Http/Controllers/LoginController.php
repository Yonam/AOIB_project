    {
        session_start();
        $username=request('username');
        $password=request('password');
           $users= utilisateur::whereLoginAndPassword($username,$password)->first();
            if(count($users) < 1){
                $mess='Login et/ou mot de passe incorrect';
                return view('user.signin')->withMess($mess);
            }
            $_SESSION['IDUSER']=$users->IDUSER;
            $_SESSION['NOM']=$users->NOMUSER;
            $_SESSION['PRENOM']=$users->PRENOMUSER;
        return redirect('dashboard');
    }

